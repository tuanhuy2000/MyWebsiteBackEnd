namespace API.Model
{
    public class Shop
    {
		private string _Id;

		public string Id
		{
			get { return _Id; }
			set { _Id = value; }
		}

		private string _Name;

		public string Name
		{
			get { return _Name; }
			set { _Name = value; }
		}

		private string _Address;

		public string Address
		{
			get { return _Address; }
			set { _Address = value; }
		}


		private string _Avatar;

		public string Avatar
		{
			get { return _Avatar; }
			set { _Avatar = value; }
		}

	}
}
